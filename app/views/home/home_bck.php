						<h3>EPC</h3>
						<p>Het energieprestatiecertificaat is een certificaat dat de energetische kwaliteit van een woning uitdrukt aan de hand van een kerngetal. Vanaf 1 januari 2009 moeten de verkopers een EPC kunnen voorleggen aan elke kandidaat koper vanaf het moment dat de woning te koop wordt aangeboden, zoniet loopt de eigenaar risico op een boete. EPC’s hebben een geldigheidsduur van 10 j. Als de woning opnieuw verkocht wordt binnen de termijn van 10 jaar, is er geen nieuw EPC nodig.   </p>
					</div>
					<div id="menu2" class="tab-pane fade">
						<h3>Keuringattest elektriciteit voor verkoop</h3>
						<p>Bij de verkoop van een woning gebouwd voor 1981 is de verkoper vanaf 1 juli 2008 verplicht om een keuringattest van de elektrische huishoudelijke installatie voor te leggen. Als de notaris niet beschikt over het attest, mag de verkoop niet doorgaan. De eigenaar van een woning gebouwd na 1981 beschikt over een attest. Een keuring is 25 jaar geldig en moet daarna opnieuw worden aangevraagd. Bij verlies dient de verkoper een nieuw attest aan te vragen. Het keuringverslag mag zowel positief als negatief zijn. Als vermeld wordt dat de installatie niet conform is, mag de woning echter wel verkocht worden. De koper heeft dan 18 maanden de tijd om alles conform te maken.</p>
					</div>
					<div id="menu3" class="tab-pane fade">
						<h3>Bodemattest</h3>
						<p>Een bodemattest dient aanwezig te zijn bij de verkoopakte van een onroerend goed. Een bodemattest is bedoeld om de kopers te informeren over de eventuele verontreinigingtoestand van de grond. </p>
					</div>
					<div id="menu4" class="tab-pane fade">
						<h3>Kadestrale legger</h3>
						<p>De kadastrale legger is een register dat per gemeente of per kadastrale afdeling van de gemeente,elke eigenaar, diens percelen, de oppervlakte alsook de kadastrale inkomens van deze percelen vermeldt. </p>
					</div>
					<div id="menu5" class="tab-pane fade">
						<h3>Kadestraal plan</h3>
						<p>Het kadastraal plan is een document dat een grafische voorstelling geeft van de onroerende eigendommen ( kadastrale percelen), van hun grenzen en van de gebouwen die erop voorkomen. </p>
					</div>
